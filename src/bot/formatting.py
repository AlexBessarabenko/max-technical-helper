"""Санитизация markdown ответов LLM под диалект Markdown мессенджера MAX.

MAX (format=markdown) понимает только инлайн-разметку: **жирный**/__жирный__,
*курсив*/_курсив_, ~~зачёркнутый~~, ++подчёркнутый++, `моноширинный`,
[ссылки](url) (dev.max.ru/docs-api, «Форматирование текста в сообщениях»).
Блочных заголовков и горизонтальных разделителей там нет, поэтому:
- заголовки `#`–`####` превращаем в жирную строку;
- разделители (`---`, `***`, `___`) заменяем пустой строкой.

Остальное (**bold**, *italic*, `code`, списки `-`/`1.`) оставляем как есть:
поддерживаемое отрендерится, списки читаемо отображаются обычным текстом.
"""

import re

# ATX-заголовок h1-h4, допускаем закрывающие решётки («## Текст ##»).
_HEADING_RE = re.compile(r"^#{1,4}\s+(.*?)(?:\s+#+\s*)?$")
# Горизонтальный разделитель: 3+ одинаковых символа -, * или _ (с пробелами).
_HR_RE = re.compile(r"^\s*([-*_])(?:\s*\1){2,}\s*$")


def to_max_markdown(text: str) -> str:
    """Преобразует markdown ответа LLM к виду, безопасному для MAX."""
    lines = []
    for line in text.splitlines():
        heading = _HEADING_RE.match(line)
        if heading:
            title = heading.group(1).strip()
            lines.append(f"**{title}**" if title else "")
        elif _HR_RE.match(line):
            lines.append("")
        else:
            lines.append(line)
    return "\n".join(lines)
